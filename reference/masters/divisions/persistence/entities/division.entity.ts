import {
  CreateDateColumn,
  Entity,
  PrimaryGeneratedColumn,
  UpdateDateColumn,
  DeleteDateColumn,
  Column,
  ManyToOne,
  JoinColumn,
  Unique,
} from 'typeorm';
import { EntityHelper } from '@/utils/entity-helper';
import { UserEntity } from '@/users/persistence/entities/user.entity';
import { StatusEnum } from '@/utils/enums/status-enum';

/**
 * TypeORM entity representing a division in the database.
 *
 * This entity defines the database structure for divisions, including
 * all necessary fields, relationships, and constraints. It extends
 * EntityHelper for common audit functionality and includes relationships
 * with user entities for division head and audit tracking.
 *
 * The entity includes database-specific features such as:
 * - Unique constraints for business keys (division_code)
 * - Foreign key relationships with proper joins
 * - Soft delete functionality with audit trails
 * - Status management with enum constraints
 * - Automatic timestamp management
 *
 * @version 3
 * @since 1.0.0
 * @author Masters Module Team
 *
 * @example
 * ```typescript
 * const entity = new DivisionEntity();
 * entity.division_code = '01';
 * entity.division_name = 'Engineering';
 * entity.division_head = userEntity;
 * ```
 */
@Entity({
  name: 'division',
})
@Unique(['division_code'])
export class DivisionEntity extends EntityHelper {
  /**
   * Unique identifier for the division.
   *
   * This field serves as the primary key for the division entity
   * and is automatically generated by the database.
   *
   * @example 1
   * @example 123
   */
  @PrimaryGeneratedColumn()
  id: number;

  /**
   * Unique business code for the division.
   *
   * This field represents the human-readable business code for the division.
   * It must be unique across all divisions and follows a specific format.
   * The code is used for business operations and reporting.
   *
   * @example '00'
   * @example '01'
   * @example '99'
   */
  @Column({
    type: 'char',
    length: 2,
    unique: true,
    nullable: false,
  })
  division_code: string;

  /**
   * Human-readable name of the division.
   *
   * This field contains the display name for the division used in
   * user interfaces and reports. It should be descriptive and
   * clearly identify the division's purpose or function.
   *
   * @example 'Engineering'
   * @example 'Marketing'
   * @example 'Sales'
   */
  @Column({
    type: String,
    length: 100,
    nullable: false,
  })
  division_name: string;

  /**
   * The user who serves as the head of the division.
   *
   * This field represents the person responsible for leading the division.
   * It establishes a many-to-one relationship with the UserEntity and
   * includes eager loading for performance optimization.
   *
   * @example UserEntity with id: 1, first_name: 'John', last_name: 'Doe'
   */
  @ManyToOne(() => UserEntity, { eager: true, nullable: false })
  @JoinColumn({ name: 'division_head' })
  division_head: UserEntity;

  /**
   * Current status of the division.
   *
   * This field indicates the operational status of the division.
   * Valid values include Active, Hold, and Cancelled. The status
   * determines whether the division is operational and available
   * for business processes.
   *
   * @example 'Active'
   * @example 'Hold'
   * @example 'Cancelled'
   */
  @Column({
    type: 'enum',
    enum: StatusEnum,
    nullable: false,
    default: StatusEnum.ACTIVE,
  })
  status: string;

  /**
   * User who created the division.
   *
   * This field tracks the user who originally created the division.
   * It establishes a many-to-one relationship with the UserEntity and
   * includes eager loading for audit purposes.
   *
   * @example UserEntity with id: 1, first_name: 'John', last_name: 'Doe'
   */
  @ManyToOne(() => UserEntity, { eager: true, nullable: false })
  @JoinColumn({ name: 'created_by' })
  created_by: UserEntity;

  /**
   * Timestamp when the division was created.
   *
   * This field records the exact date and time when the division
   * was first created in the system. It's automatically managed
   * by TypeORM and used for audit purposes.
   *
   * @example "2024-01-15T10:30:00.000Z"
   */
  @CreateDateColumn()
  created_at: Date;

  /**
   * User who last updated the division.
   *
   * This field tracks the user who made the most recent changes
   * to the division. It establishes a many-to-one relationship
   * with the UserEntity and includes eager loading for audit purposes.
   *
   * @example UserEntity with id: 1, first_name: 'John', last_name: 'Doe'
   */
  @ManyToOne(() => UserEntity, { eager: true, nullable: false })
  @JoinColumn({ name: 'updated_by' })
  updated_by: UserEntity;

  /**
   * Timestamp when the division was last updated.
   *
   * This field records the exact date and time when the division
   * was last modified. It's automatically managed by TypeORM
   * and used for audit purposes and change tracking.
   *
   * @example "2024-01-15T14:30:00.000Z"
   */
  @UpdateDateColumn()
  updated_at: Date;

  /**
   * User who deleted the division (if applicable).
   *
   * This field tracks the user who performed the soft delete operation
   * on the division. It's null for active divisions and contains user
   * information for deleted divisions. Includes eager loading for audit purposes.
   *
   * @example null
   * @example UserEntity with id: 1, first_name: 'John', last_name: 'Doe'
   */
  @ManyToOne(() => UserEntity, { eager: true, nullable: true })
  @JoinColumn({ name: 'deleted_by' })
  deleted_by?: UserEntity | null;

  /**
   * Timestamp when the division was deleted (if applicable).
   *
   * This field records the exact date and time when the division
   * was soft deleted. It's null for active divisions and contains
   * the deletion timestamp for deleted divisions. Automatically
   * managed by TypeORM for soft delete functionality.
   *
   * @example null
   * @example "2024-01-15T16:30:00.000Z"
   */
  @DeleteDateColumn()
  deleted_at?: Date | null;
}
