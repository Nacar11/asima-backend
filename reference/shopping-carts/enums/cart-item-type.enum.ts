/**
 * Cart item type enumeration.
 *
 * Defines the type of item that can be added to a shopping cart.
 * - PRODUCT: Physical or digital product variant
 * - SERVICE: Service booking with optional package
 *
 * @version 1
 * @since 1.0.0
 */
export enum CartItemTypeEnum {
  PRODUCT = 'product',
  SERVICE = 'service',
}
